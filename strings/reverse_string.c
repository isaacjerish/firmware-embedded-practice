/*
 * Function: reverseStringInPlace
 * ------------------------------
 * Reverses a null-terminated C-style string in place.
 *
 * str: A pointer to the C-style string (character array) to be reversed.
 * The string is null-terminated.
 *
 * Behavior:
 * - The function should modify the input string directly.
 * - If the input string pointer is NULL or if the string is empty
 * (i.e., its first character is the null terminator '\0'),
 * the function should not cause an error and can simply return.
 *
 * Example 1:
 * char s1[] = "hello";
 * reverseStringInPlace(s1);
 * // s1 should now be "olleh"
 */